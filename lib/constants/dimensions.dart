import 'package:flutter/material.dart';

SizedBox height10() {
  return const SizedBox(
    height: 10,
  );
}

SizedBox width10() {
  return const SizedBox(
    width: 10,
  );
}

SizedBox height20() {
  return const SizedBox(
    height: 20,
  );
}

SizedBox width20() {
  return const SizedBox(
    width: 20,
  );
}

SizedBox sizedHeight(double height) {
  return SizedBox(
    height: height,
  );
}

SizedBox sizedWidth(double width) {
  return SizedBox(
    width: width,
  );
}
